public class InvitacionGrupal {

    private String anfritrion;
    private String viajeGrupal;
    private String nombre;

    public InvitacionGrupal() {
    }

    public String getAnfritrion() {
        return anfritrion;
    }

    public void setAnfritrion(String anfritrion) {
        this.anfritrion = anfritrion;
    }

    public String getViajeGrupal() {
        return viajeGrupal;
    }

    public void setViajeGrupal(String viajeGrupal) {
        this.viajeGrupal = viajeGrupal;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
